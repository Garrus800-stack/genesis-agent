#!/usr/bin/env node
// ============================================================
// GENESIS — scripts/benchmark-readme.js (v6.0.1)
//
// Reads benchmark results from .genesis/benchmark-latest.json
// and writes a formatted markdown table into README.md.
//
// Usage:
//   node scripts/benchmark-readme.js           — update README
//   node scripts/benchmark-readme.js --dry-run — preview without writing
//   node scripts/benchmark-readme.js --file <path> — use custom results file
//
// The script looks for markers in README.md:
//   <!-- BENCHMARK-START -->
//   ...generated content...
//   <!-- BENCHMARK-END -->
//
// If markers don't exist, the script adds them before
// the "## Documentation" section.
//
// Designed to run after: npm run benchmark:agent
// npm script: npm run benchmark:readme
// ============================================================

'use strict';

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const README_PATH = path.join(ROOT, 'README.md');
const DEFAULT_RESULTS = path.join(ROOT, '.genesis', 'benchmark-latest.json');

const args = process.argv.slice(2);
const dryRun = args.includes('--dry-run');
const fileIdx = args.indexOf('--file');
const resultsPath = fileIdx >= 0 ? args[fileIdx + 1] : DEFAULT_RESULTS;

const START_MARKER = '<!-- BENCHMARK-START -->';
const END_MARKER = '<!-- BENCHMARK-END -->';

// ── Category icons ──────────────────────────────────────────
const TYPE_ICONS = {
  'code-gen': 'code-gen',
  'bug-fix': 'bug-fix',
  'refactoring': 'refactor',
  'analysis': 'analysis',
  'chat': 'chat',
};

function main() {
  // 1. Read results
  if (!fs.existsSync(resultsPath)) {
    console.error(`[BENCHMARK-README] Results file not found: ${resultsPath}`);
    console.error('  Run "npm run benchmark:agent" first.');
    process.exit(1);
  }

  const raw = fs.readFileSync(resultsPath, 'utf8');
  const data = JSON.parse(raw);

  if (!data.results || !Array.isArray(data.results)) {
    console.error('[BENCHMARK-README] Invalid results format — missing results array.');
    process.exit(1);
  }

  // 2. Generate markdown
  const md = generateMarkdown(data);

  if (dryRun) {
    console.log('[BENCHMARK-README] Dry run — preview:\n');
    console.log(md);
    return;
  }

  // 3. Read README
  if (!fs.existsSync(README_PATH)) {
    console.error(`[BENCHMARK-README] README.md not found: ${README_PATH}`);
    process.exit(1);
  }

  let readme = fs.readFileSync(README_PATH, 'utf8');

  // 4. Replace or insert
  if (readme.includes(START_MARKER) && readme.includes(END_MARKER)) {
    // Replace between markers
    const startIdx = readme.indexOf(START_MARKER);
    const endIdx = readme.indexOf(END_MARKER) + END_MARKER.length;
    readme = readme.slice(0, startIdx) + START_MARKER + '\n' + md + '\n' + END_MARKER + readme.slice(endIdx);
    console.log('[BENCHMARK-README] Updated existing benchmark section in README.md');
  } else {
    // Insert before ## Documentation (or at end)
    const insertBefore = '## Documentation';
    const insertIdx = readme.indexOf(insertBefore);
    const block = '\n' + START_MARKER + '\n' + md + '\n' + END_MARKER + '\n\n';
    if (insertIdx >= 0) {
      readme = readme.slice(0, insertIdx) + block + readme.slice(insertIdx);
      console.log('[BENCHMARK-README] Inserted benchmark section before ## Documentation');
    } else {
      readme += block;
      console.log('[BENCHMARK-README] Appended benchmark section to end of README.md');
    }
  }

  // 5. Write
  fs.writeFileSync(README_PATH, readme, 'utf8');
  console.log(`[BENCHMARK-README] ${data.results.length} tasks written. ${data.passed}/${data.tasks} passed (${data.successRate}%).`);
}

function generateMarkdown(data) {
  const lines = [];

  lines.push(`### Agent Benchmark Results`);
  lines.push('');
  lines.push(`> Auto-generated by \`npm run benchmark:readme\` from \`.genesis/benchmark-latest.json\`.`);
  lines.push(`> Last run: ${formatDate(data.timestamp)} | Backend: \`${data.backend}\` | Version: v${data.version}`);
  lines.push('');

  // Summary line
  lines.push(`**${data.passed}/${data.tasks} passed (${data.successRate}%)** — ${formatMs(data.totalMs)} total, ~${formatTokens(data.totalTokens)} tokens`);
  lines.push('');

  // Per-task table
  lines.push('| Task | Category | Result | Duration | Tokens |');
  lines.push('|------|----------|--------|----------|--------|');

  for (const r of data.results) {
    const icon = r.success ? '✅' : '❌';
    const cat = TYPE_ICONS[r.type] || r.type || '—';
    const dur = formatMs(r.durationMs);
    const tok = r.tokenEstimate ? `~${formatTokens(r.tokenEstimate)}` : '—';
    const title = (r.title || r.id).slice(0, 50);
    lines.push(`| ${title} | ${cat} | ${icon} | ${dur} | ${tok} |`);
  }

  lines.push('');

  // Category breakdown
  const byType = {};
  for (const r of data.results) {
    const t = r.type || 'other';
    if (!byType[t]) byType[t] = { total: 0, passed: 0 };
    byType[t].total++;
    if (r.success) byType[t].passed++;
  }

  lines.push('**By category:** ' + Object.entries(byType)
    .map(([type, stats]) => `${type} ${stats.passed}/${stats.total}`)
    .join(' · '));
  lines.push('');

  return lines.join('\n');
}

function formatDate(ts) {
  if (!ts) return 'unknown';
  return ts.replace('T', ' ').slice(0, 16);
}

function formatMs(ms) {
  if (!ms) return '—';
  if (ms >= 60000) return `${(ms / 60000).toFixed(1)}min`;
  if (ms >= 1000) return `${(ms / 1000).toFixed(1)}s`;
  return `${ms}ms`;
}

function formatTokens(n) {
  if (!n) return '0';
  if (n >= 1000) return `${(n / 1000).toFixed(1)}k`;
  return String(n);
}

main();
